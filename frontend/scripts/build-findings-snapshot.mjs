#!/usr/bin/env node
/**
 * Snapshot the QA findings files into a TypeScript module that ships with
 * the Next.js bundle.
 *
 * Why this exists: the findings page reads the contents of six markdown
 * files that live OUTSIDE the frontend/ directory (qa/findings.md plus
 * qa/specialists/<name>/findings.md, at the repo root). When Vercel
 * builds the Next.js project, it only uploads files under the project's
 * root directory (frontend/), so a build-time fs.readFileSync of
 * '../qa/...' works locally but fails on Vercel with
 * 'no such file or directory'.
 *
 * Solution: this script runs BEFORE next build (wired via the prebuild
 * npm lifecycle hook) and produces app/findings/data.generated.ts, which
 * exports the markdown contents as plain strings. The Next.js build then
 * sees a self-contained source tree and doesn't need any files outside
 * frontend/.
 *
 * The generated file is committed-or-gitignored at the caller's discretion;
 * we gitignore it so the source of truth stays in qa/.
 *
 * Run order:
 *   - npm run build  -> npm runs prebuild -> this script -> next build
 *   - npm run dev    -> npm runs predev   -> this script -> next dev
 *
 * If qa/ is unreachable (e.g., the script is somehow run from a non-repo
 * checkout), we emit the file with [unavailable] placeholders so the
 * Next.js build still succeeds. The page surfaces the warning visibly.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// scripts/ lives at frontend/scripts; repo root is two levels up
const REPO_ROOT = resolve(__dirname, "..", "..");
const OUT_PATH = resolve(__dirname, "..", "app", "findings", "data.generated.ts");

const SOURCES = [
  { key: "manager", path: "qa/findings.md" },
  { key: "api-tester", path: "qa/specialists/api-tester/findings.md" },
  { key: "security-tester", path: "qa/specialists/security-tester/findings.md" },
  { key: "ui-tester", path: "qa/specialists/ui-tester/findings.md" },
  { key: "data-tester", path: "qa/specialists/data-tester/findings.md" },
  { key: "exploratory-tester", path: "qa/specialists/exploratory-tester/findings.md" },
];

const PLACEHOLDER = (path) =>
  `# (unavailable)\n\n_This file was not present in the build context at \`${path}\`._\n`;

const entries = SOURCES.map((s) => {
  const absolute = join(REPO_ROOT, s.path);
  let markdown;
  let available = false;
  if (existsSync(absolute)) {
    try {
      markdown = readFileSync(absolute, "utf8");
      available = true;
    } catch (err) {
      markdown = PLACEHOLDER(s.path) + `\n_Read error: ${String(err)}_\n`;
    }
  } else {
    markdown = PLACEHOLDER(s.path);
  }
  return { key: s.key, path: s.path, available, markdown };
});

mkdirSync(dirname(OUT_PATH), { recursive: true });

const header = `// Generated by scripts/build-findings-snapshot.mjs. Do not edit by hand.
// Source of truth lives in the repo's \`qa/\` directory; this file is rebuilt
// before every \`next dev\` / \`next build\` so the contents stay in sync.

export type FindingsSnapshot = {
  key: string;
  path: string;
  available: boolean;
  markdown: string;
};

export const FINDINGS_SNAPSHOT: FindingsSnapshot[] = ${JSON.stringify(entries, null, 2)};

export const FINDINGS_SNAPSHOT_BUILT_AT = ${JSON.stringify(new Date().toISOString())};
`;

writeFileSync(OUT_PATH, header, "utf8");

const summary = entries
  .map((e) => `  ${e.available ? "✓" : "✗"} ${e.key.padEnd(20)} ${e.path}`)
  .join("\n");
console.log(`[findings-snapshot] wrote ${OUT_PATH}`);
console.log(summary);
